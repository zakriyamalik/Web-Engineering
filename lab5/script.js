  $("#firstEle").click(function(){
    if(this.style.color == "yellow")
    {
        $(".intro1").css("display","none");
        $(this).css("color", "white");
    }
    else
    {
        $(this).css("color", "yellow");
        $(".intro1").css("display","block");
        $("#bodydiv").addclass("intro1");
       
    }


    
  });

